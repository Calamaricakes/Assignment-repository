package textBookExercise;

public class BMI {
	public static void main(String[] args){
		double weightInKilograms = 50;
		double heightInMeters = 1.75;
		System.out.println("Weight in kilograms = " + weightInKilograms);
		System.out.println("Height in meters = " + heightInMeters);
		System.out.println("BMI = " + weightInKilograms/heightInMeters);
	}
}
